
public class Regra {
	
	public Defeitos def;
	public int x;
	public int y;
	
	public Regra(Defeitos def, int x, int y) {
		this.def=def;
		this.x=x;
		this.y=y;
	}

	public Defeitos getDef() {
		return def;
	}

	public void setDef(Defeitos def) {
		this.def = def;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	
	
	
}
